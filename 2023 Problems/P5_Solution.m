% INPUTS///////////////////////////////////////////////////////////////////
N = 27;
starter = "Selin";
% /////////////////////////////////////////////////////////////////////////

% SOLUTION/////////////////////////////////////////////////////////////////
C = [0, 0, N];
i = 1;
while C(3) > 0
    if mod(C(3), 2) == 0
        if mod(C(3) / 2, 2) == 0 && C(3) > 4
            C(2 - mod(i, 2)) = C(2 - mod(i, 2)) + 1;
            C(3) = C(3) - 1;
        else
            C(2 - mod(i, 2)) = C(2 - mod(i, 2)) + (C(3) / 2);
            C(3) = C(3) / 2;
        end
    else
        C(2 - mod(i, 2)) = C(2 - mod(i, 2)) + 1;
        C(3) = C(3) - 1;
    end
    i = i + 1;
end

if starter == "Selin"
    C(1 : 2) = fliplr(C(1 : 2));
end

if C(1) > C(2)
    result = [C(1 : 2), "Emre"];
else
    result = [C(1 : 2), "Selin"];
end

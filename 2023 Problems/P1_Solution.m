% INPUTS///////////////////////////////////////////////////////////////////
L_init = ['G', '-', 'R';
          '-', '-', '-';
          'R', '-', 'G'];
t = 75;
% /////////////////////////////////////////////////////////////////////////

% SOLUTION/////////////////////////////////////////////////////////////////
round = ceil(t / 31);
t_mod = mod(t, 31);
if t_mod == 0
    L = L_init;
    L(logical((L == 'R') + (L == 'G'))) = 'Y';
else
    if mod(round, 2) == 1
        L = L_init;
    else
        L = flip(L_init);
    end
end
% /////////////////////////////////////////////////////////////////////////